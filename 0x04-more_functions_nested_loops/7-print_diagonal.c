/**
 * print_diagonal -> prints a diagonal line
 * @n: number of a line
 * Return: void
 */

void print_diagonal(int n)
{
	int i;
	
	for (i = 0; i < n; i++)
	{
		_putchar(32);
		_putchar(92);
		
	}
	_putchar('\n');                                                                                                                       

}
